// scripts/apply-migrations.js
// Applies specific pending migrations via Supabase Management API.
// Usage: SUPABASE_ACCESS_TOKEN=sbp_... node scripts/apply-migrations.js

const fs    = require('fs');
const path  = require('path');
const https = require('https');

const PROJECT_REF  = 'gsnbpfpekqqjlmkgvwvb';
const ACCESS_TOKEN = process.env.SUPABASE_ACCESS_TOKEN || '';

// Pending migrations in apply order
const PENDING = [
  'LAUNCH_apply_all.sql',
  '20260429_deal_intelligence_layer.sql',
  '20260503_scanned_products.sql',
  '20260503_adaptive_memory.sql',
  '20260503_trip_feedback.sql',
  '20260503_nutrition_intelligence.sql',
  '20260504_savings_loop.sql',
  '20260504_quick_start_flow.sql',
];

const MIGRATIONS_DIR = path.join(__dirname, '..', 'supabase', 'migrations');

// ── SQL pre-processing ──────────────────────────────────────────────────────

// Fix 1: ARRAY['a','b'] literals inserted into jsonb columns → '["a","b"]'::jsonb
// Affects: LAUNCH_apply_all.sql (dietary_tags is jsonb, not text[])
function fixArrayLiterals(sql) {
  return sql.replace(/ARRAY\[([^\]]+)\]/g, (_, items) => {
    const values = items.split(',').map(s => s.trim().replace(/^'|'$/g, ''));
    return "'" + JSON.stringify(values) + "'::jsonb";
  });
}

// Fix 2: Remove final_price from INSERT INTO stack_candidates — it's a GENERATED column.
function removeGeneratedFinalPrice(sql) {
  sql = sql.replace(/,\s*\bfinal_price\b\s*(?=,\s*stack_rank_score)/g, '');
  sql = sql.replace(/\s*ahf\.pay_price\s+AS\s+final_price,?\n/g, '\n');
  return sql;
}

// Fix 3: savings_pct on stack_candidates is NUMERIC(5,4) — stores 0–1 fraction, not 0–100%.
// The SELECT calculates it as * 100, which overflows. Remove the multiplication.
function fixSavingsPctFraction(sql) {
  // Change:  (ahf.save_price / NULLIF(ahf.original_price, 0)) * 100, 1)  AS savings_pct
  // To:      (ahf.save_price / NULLIF(ahf.original_price, 0)), 4)  AS savings_pct
  sql = sql.replace(
    /ROUND\(\s*\(ahf\.save_price\s*\/\s*NULLIF\(ahf\.original_price,\s*0\)\)\s*\*\s*100,\s*1\s*\)\s*AS\s+savings_pct/g,
    'ROUND((ahf.save_price / NULLIF(ahf.original_price, 0)), 4) AS savings_pct'
  );
  return sql;
}

// Fix 3: Add DROP POLICY IF EXISTS before each CREATE POLICY so re-runs are safe.
// Uses /gi so it matches regardless of case (some files use lowercase 'create policy').
function dropPoliciesBeforeCreate(sql) {
  return sql.replace(
    /\b((?:CREATE|create)\s+(?:POLICY|policy)\s+"([^"]+)"\s+[Oo][Nn]\s+(\w+))/g,
    (match, _full, policyName, tableName) =>
      `DROP POLICY IF EXISTS "${policyName}" ON ${tableName};\n${match}`
  );
}

// Fix 4: Add unique constraint on home_payload_cache.cache_key before the ON CONFLICT INSERT.
function fixHomePayloadCacheConflict(sql) {
  return sql.replace(
    /INSERT INTO public\.home_payload_cache \(cache_key/,
    'CREATE UNIQUE INDEX IF NOT EXISTS home_payload_cache_cache_key_uniq\n  ON public.home_payload_cache (cache_key);\n\nINSERT INTO public.home_payload_cache (cache_key'
  );
}

// Fix 5: Strip seed-only INSERTs from LAUNCH_apply_all that fail due to schema mismatches.
// stack_candidates is re-populated by the weekly-refresh cron.
// digital_coupons column schema differs from what this seed INSERT expects.
function removeSeedOnlyInserts(sql) {
  sql = sql.replace(
    /INSERT INTO public\.stack_candidates[\s\S]*?ON CONFLICT \(dedupe_key\) DO NOTHING;/,
    '-- stack_candidates seed removed (regenerated by cron)'
  );
  sql = sql.replace(
    /INSERT INTO public\.digital_coupons[\s\S]*?ON CONFLICT DO NOTHING;/,
    '-- digital_coupons seed removed (column schema mismatch)'
  );
  sql = sql.replace(
    /INSERT INTO public\.home_payload_cache[\s\S]*?SET payload\s*=\s*EXCLUDED\.payload,\s*updated_at\s*=\s*EXCLUDED\.updated_at;/,
    '-- home_payload_cache seed removed (user_id NOT NULL; cache rebuilt by cron)'
  );
  return sql;
}

function preprocessSQL(filename, sql) {
  if (filename === 'LAUNCH_apply_all.sql') {
    sql = fixArrayLiterals(sql);
    sql = removeGeneratedFinalPrice(sql);
    sql = fixSavingsPctFraction(sql);
    sql = removeSeedOnlyInserts(sql);
    sql = fixHomePayloadCacheConflict(sql);
  }
  if (
    filename === '20260503_adaptive_memory.sql' ||
    filename === '20260503_trip_feedback.sql' ||
    filename === '20260503_nutrition_intelligence.sql' ||
    filename === '20260504_savings_loop.sql'
  ) {
    sql = dropPoliciesBeforeCreate(sql);
  }
  return sql;
}

// ── HTTP helper ─────────────────────────────────────────────────────────────

function postQuery(sql) {
  return new Promise((resolve, reject) => {
    const body = JSON.stringify({ query: sql });
    const options = {
      hostname: 'api.supabase.com',
      port: 443,
      path: `/v1/projects/${PROJECT_REF}/database/query`,
      method: 'POST',
      headers: {
        'Authorization': `Bearer ${ACCESS_TOKEN}`,
        'Content-Type': 'application/json',
        'Content-Length': Buffer.byteLength(body),
      },
    };
    const req = https.request(options, res => {
      let data = '';
      res.on('data', chunk => data += chunk);
      res.on('end', () => {
        try { resolve({ status: res.statusCode, body: JSON.parse(data) }); }
        catch (_) { resolve({ status: res.statusCode, body: data }); }
      });
    });
    req.on('error', reject);
    req.write(body);
    req.end();
  });
}

// ── Runner ───────────────────────────────────────────────────────────────────

async function run() {
  if (!ACCESS_TOKEN) { console.error('ERROR: SUPABASE_ACCESS_TOKEN not set'); process.exit(1); }
  console.log(`Applying ${PENDING.length} migrations to project ${PROJECT_REF}\n`);

  let passed = 0, skipped = 0, failed = 0;

  for (const filename of PENDING) {
    const filepath = path.join(MIGRATIONS_DIR, filename);
    if (!fs.existsSync(filepath)) {
      console.log(`  SKIP   ${filename} — file not found`);
      skipped++;
      continue;
    }

    let sql = fs.readFileSync(filepath, 'utf8');
    sql = preprocessSQL(filename, sql);

    process.stdout.write(`  apply  ${filename} ... `);
    try {
      const result = await postQuery(sql);
      if (result.status === 200 || result.status === 201) {
        console.log('OK');
        passed++;
      } else {
        const msg = result.body?.message || result.body?.error || JSON.stringify(result.body);
        console.log(`FAILED (${result.status}): ${msg}`);
        failed++;
      }
    } catch (err) {
      console.log(`ERROR: ${err.message}`);
      failed++;
    }
  }

  console.log(`\n${passed} applied  ${skipped} skipped  ${failed} failed`);
  if (failed > 0) process.exit(1);
}

run();
